function y = fwdSubst(L, b, k)

% Forward substitution
% y = fwdSubst(L, b, k) solves Ly = b by forward substitution method
% implemented using recursion.

% =========================================================================
%                            Forward Substitution
% =========================================================================

% -------------------------------------------------------------------------
% Find the size of matrix L
% -------------------------------------------------------------------------

[m, n] = size(L);

% -------------------------------------------------------------------------
% If first call k = 1
% -------------------------------------------------------------------------

if(nargin == 2)
    k = 1;
end

% -------------------------------------------------------------------------
% Find y
% -------------------------------------------------------------------------

y = b(k)/L(k,k);

% -------------------------------------------------------------------------
% Apply recursion.
% -------------------------------------------------------------------------

if(k < n)
    l = [zeros(k,1);L(k+1:m,k)];
    y = [y;fwdSubst(L,b-y*l,k+1)];
end

end

% =========================================================================


